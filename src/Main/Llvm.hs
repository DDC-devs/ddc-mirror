llvmOfReturn :: Exp a -> IO [LlvmStatement]
llvmOfReturn (XVar v t)
 | t == TPtr TObj
 =	return [ Return (Just (toLlvmVar v t)) ]


llvmOfReturn x
 = do	return	$ [ Comment ["Return (" ++ (takeWhile (/= ' ') (show x)) ++ ")"]
		  , Return Nothing ]

